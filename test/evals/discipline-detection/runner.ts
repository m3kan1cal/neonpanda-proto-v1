#!/usr/bin/env tsx

/**
 * Discipline Detection Eval Runner
 *
 * Runs the labeled prompts in `fixtures.json` through the in-process
 * `detectDiscipline` function and compares against `baseline.json`.
 *
 * Usage:
 *   tsx test/evals/discipline-detection/runner.ts               # evaluate vs baseline
 *   tsx test/evals/discipline-detection/runner.ts --update-baseline  # regenerate baseline
 *
 * Exit codes:
 *   0 — all fixtures matched their expected discipline AND no regression vs
 *       the committed baseline.
 *   1 — at least one fixture regressed vs baseline (was passing, now failing)
 *       OR the baseline is empty and --update-baseline was not passed.
 *
 * The runner is intentionally dependency-free (no test framework) so it can
 * be invoked from CI with `tsx` and from local dev without extra setup. It
 * hits the real LLM via `detectDiscipline`, so expect it to take time and
 * cost tokens — run it sparingly, not on every commit.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { detectDiscipline } from "../../../amplify/functions/libs/workout/discipline-detector.ts";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

interface Fixture {
  id: string;
  expected: string;
  prompt: string;
  notes?: string;
}

interface FixturesFile {
  version: number;
  description?: string;
  fixtures: Fixture[];
}

interface BaselineResult {
  predicted: string;
  pass: boolean;
}

interface BaselineFile {
  version: number;
  generatedAt: string | null;
  notes?: string;
  results: Record<string, BaselineResult>;
}

const FIXTURES_PATH = path.join(__dirname, "fixtures.json");
const BASELINE_PATH = path.join(__dirname, "baseline.json");

function loadJson<T>(p: string): T {
  return JSON.parse(fs.readFileSync(p, "utf8")) as T;
}

function writeJson(p: string, data: unknown): void {
  fs.writeFileSync(p, JSON.stringify(data, null, 2) + "\n", "utf8");
}

async function runOne(f: Fixture): Promise<BaselineResult> {
  const result = await detectDiscipline(f.prompt);
  const predicted = result.discipline;
  return {
    predicted,
    pass: predicted === f.expected,
  };
}

async function main(): Promise<void> {
  const updateBaseline = process.argv.includes("--update-baseline");

  const fixturesFile = loadJson<FixturesFile>(FIXTURES_PATH);
  const baseline = loadJson<BaselineFile>(BASELINE_PATH);

  const results: Record<string, BaselineResult> = {};
  let expectedFailures = 0;

  for (const f of fixturesFile.fixtures) {
    process.stdout.write(`• ${f.id} (${f.expected}) … `);
    try {
      const r = await runOne(f);
      results[f.id] = r;
      const marker = r.pass ? "OK" : `MISS -> ${r.predicted}`;
      process.stdout.write(`${marker}\n`);
      if (!r.pass) expectedFailures++;
    } catch (err) {
      const message = err instanceof Error ? err.message : String(err);
      results[f.id] = { predicted: "error", pass: false };
      process.stdout.write(`ERROR: ${message}\n`);
      expectedFailures++;
    }
  }

  if (updateBaseline) {
    const newBaseline: BaselineFile = {
      version: 1,
      generatedAt: new Date().toISOString(),
      notes:
        "Auto-generated by runner.ts. Do not hand-edit; re-run with --update-baseline after deliberate prompt changes.",
      results,
    };
    writeJson(BASELINE_PATH, newBaseline);
    console.log(
      `\nBaseline updated at ${BASELINE_PATH} with ${Object.keys(results).length} fixtures (${expectedFailures} still missing expected label).`,
    );
    process.exit(0);
  }

  const baselineIsEmpty =
    !baseline.results || Object.keys(baseline.results).length === 0;
  if (baselineIsEmpty) {
    console.error(
      "\nNo baseline committed yet. Regenerate with --update-baseline and commit baseline.json before enforcing.",
    );
    process.exit(1);
  }

  let regressions = 0;
  for (const [id, current] of Object.entries(results)) {
    const prior = baseline.results[id];
    if (!prior) continue;
    if (prior.pass && !current.pass) {
      regressions++;
      console.error(
        `  REGRESSION ${id}: baseline passed (${prior.predicted}), now ${current.predicted}.`,
      );
    }
  }

  if (regressions > 0) {
    console.error(
      `\n${regressions} regression(s) vs baseline. Investigate or re-baseline deliberately.`,
    );
    process.exit(1);
  }

  console.log(
    `\nAll ${Object.keys(results).length} fixtures evaluated. No regressions vs committed baseline. ${expectedFailures} fixture(s) still missing their expected label (tracked in baseline).`,
  );
  process.exit(0);
}

main().catch((err) => {
  console.error("Fatal runner error:", err);
  process.exit(1);
});
